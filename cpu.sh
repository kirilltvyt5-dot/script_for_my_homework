#!/bin/bash

# Путь к файлу лога (в той же директории, что и скрипт)
LOG_FILE="cpu.log"

# Порог нагрузки CPU (в процентах)
THRESHOLD=60

# Функция для получения текущей нагрузки CPU (средняя за последние 1-5 минут)
get_cpu_load() {
    # Используем uptime и парсим среднюю нагрузку за 1 минуту
    # Или можно использовать top в неинтерактивном режиме
    # Вариант с top (более точный для текущей нагрузки)
    top -b -n 1 | grep "Cpu(s)" | awk '{print $2 + $4}'
}

# Функция для записи в лог
log_event() {
    echo "$(date '+%Y-%m-%d %H:%M:%S') - CPU load: $1%" >> "$LOG_FILE"
}

# Основной цикл мониторинга
echo "Мониторинг CPU нагрузки. Нажмите Ctrl+C для остановки."

while true; do
    # Получаем текущую нагрузку CPU
    cpu_load=$(get_cpu_load)
    
    # Убираем возможные пробелы и округляем до целого числа
    cpu_load=$(printf "%.0f" "$cpu_load")
    
    # Проверяем, превышена ли нагрузка
    if (( $(echo "$cpu_load > $THRESHOLD" | bc -l) )); then
        log_event "$cpu_load"
        echo "ВНИМАНИЕ: Нагрузка CPU $cpu_load% превысила порог $THRESHOLD% - записано в $LOG_FILE"
    else
        # Опционально: можно выводить статус, если нужно отслеживать
        # echo "CPU: $cpu_load% (норма)"
    fi
    
    # Ждем 10 секунд перед следующей проверкой
    sleep 10
done
